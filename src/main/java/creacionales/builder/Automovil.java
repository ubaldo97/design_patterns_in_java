package creacionales.builder;

//Regularmente suele ser una clase abstracta
public class Automovil {
    public String marca;
    public String tipo;
    public String motor;
    public int puertas;

    public int getPuertas() {
        return puertas;
    }

    public String getMarca() {
        return marca;
    }

    public String getTipo() {
        return tipo;
    }

    public String getMotor() {
        return motor;
    }

}
